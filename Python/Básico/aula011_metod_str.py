mensagem = ' eu adoro comida Japonesa'
#index      0123456789
print(mensagem.upper()) # Maiusculo
print(mensagem.lower()) # Minusculo
print(mensagem.capitalize()) # Primeira letra maiuscula
print(mensagem.find('c')) # Achar a posição da primeira letra c
print(mensagem.find('J')) # Achar a posição da primeira letra J
print(mensagem.find('adoro')) # Achar o começo da palavra adoro
print(mensagem.replace('a','e')) # Trocar toda letra a por e
print(mensagem.replace('Japonesa', 'Japa')) # Trocar a palavra Japonesa por Japa
print(mensagem.strip()) # Tirar os espaços